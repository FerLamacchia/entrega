let entrada =prompt("Ingrese un número");

while (entrada != "ESC") {

    switch (entrada) {

        case "1":
            alert ("Tomate");
        break;

        case "2":
            alert ("Papa");
            break;

            case "3":
                alert ("Carne");
                break;

            case "4":
                alert ("Pollo");
                break;

            default:
                alert ("Numero ingresado incorrecto");
                break;





    }

    entrada =prompt("Ingrese un número");
}



